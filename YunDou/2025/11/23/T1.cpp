int op(int a, int b) { return max(a, b); }
int e() { return -0x3f3f3f3f; }
int mapping(int f, int x) { return x + f; }
int composition(int f, int g) { return f + g; }
int id() { return 0; }
atcoder::lazy_segtree<int, op, e, int, mapping, composition, id> seg(N);
int pre[N], suf[N], ans[N];
int main() {
    freopen("ayuzawa.in", "r", stdin);
    freopen("ayuzawa.out", "w", stdout);
    ios::sync_with_stdio(false), cin.tie(nullptr);
    cin >> n;
    seg.set(0, 0);
    seg.set(n + 1, 0);
    stack<int> s;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        while (s.size() && a[s.top()] <= a[i]) s.pop();
        if (s.size()) pre[i] = s.top();
        s.push(i);
    }
    while (s.size()) s.pop();
    for (int i = n; i >= 1; i--) {
        while (s.size() && a[s.top()] <= a[i]) s.pop();
        if (s.size()) suf[i] = s.top();
        else suf[i] = n + 1;
        s.push(i);
    }
    for (int i = 1; i <= n; i++) seg.set(i, seg.get(i - 1) + a[i]);
    for (int i = 1; i <= n; i++) {
        // add(i, n, -a[i]);
        seg.apply(i, n + 1, -a[i]);
        ans[i] = max(seg.prod(i + 1, suf[i]), 0);
        // query(i + 1, suf[i] - 1);
    }
    for (int i = n; i >= 1; i--) seg.set(i, seg.get(i + 1) + a[i]);
    for (int i = n; i >= 1; i--) {
        // add(1, i, -a[i]);
        seg.apply(1, i + 1, -a[i]);
        ans[i] += max(seg.prod(pre[i] + 1, i), 0);
        // query(pre[i] + 1, i - 1);
    }
    int mx = 0;
    for (int i = 1; i <= n; i++) mx = max(mx, ans[i]);
    cout << mx << endl;
    return 0;
}